// Now we will use different Arithmetic operations here i.e +,-,*,/.

#include<stdio.h>
int main()
{
	float a = 2;
	float b = 3;
	  					
 	printf("%f\n",a+b);
 	printf("%f\n",a-b);   //here we need an answer in Float so we will use %f.
 	printf("%f\n",a*b);
 	                    
                   
 	printf("%f",a/b);
 	
 	return 0;
}
